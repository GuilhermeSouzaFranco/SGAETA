    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("".equals(tfCurso.getText()) && cbModalidade.getSelectedItem() == "") {
            JOptionPane.showMessageDialog(rootPane, "Erro! Preencha todos os campos");
        } else {
            c.setCurso(tfCurso.getText());
            c.setModalidade((String) cbModalidade.getSelectedItem());
            daoCurso.salvar(c);
            tfCurso.setText(null);
            cbModalidade.setSelectedItem(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

